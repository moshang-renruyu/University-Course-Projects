"""
字典的定义，同样使用{}，不过存储的元素是一个个的：键值对，如下语法：
使用{}存储原始，每一个元素是一个键值对
每一个键值对包含Key和Value（用冒号分隔）
键值对之间使用逗号分隔
Key和Value可以是任意类型的数据（key不可为字典）
Key不可重复，重复会对原有数据覆盖
字典同集合一样，不可以使用下标索引
但是字典可以通过Key值来取得对应的Value
"""
dic1={"薛婧萱":100,"薛锐":88}
dic2={}
dic3=dict()
score1=dic1["薛婧萱"]
print(score1)
"""
字典的Key和Value可以是任意数据类型（Key不可为字典）
那么，就表明，字典是可以嵌套的
"""
dic4={"薛婧萱": {"语文":100,"数学":150},"薛锐": {"语文":88,"数学":10}}
print(dic4)
print(dic4["薛婧萱"])
print(dic4["薛婧萱"]["语文"])
